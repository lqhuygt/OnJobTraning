package OOP;

public class KySu extends  canbo {
    public KySu() {
        super();
    }

    public KySu(String hoTen, String namSinh, String gioiTinh, String diaChi) {
        super(hoTen, namSinh, gioiTinh, diaChi);
    }

    @Override
    public String getHoTen() {
        return super.getHoTen();
    }

    @Override
    public void setHoTen(String hoTen) {
        super.setHoTen(hoTen);
    }

    @Override
    public String getNamSinh() {
        return super.getNamSinh();
    }

    @Override
    public void setNamSinh(String namSinh) {
        super.setNamSinh(namSinh);
    }

    @Override
    public String getGioiTinh() {
        return super.getGioiTinh();
    }

    @Override
    public void setGioiTinh(String gioiTinh) {
        super.setGioiTinh(gioiTinh);
    }

    @Override
    public String getDiaChi() {
        return super.getDiaChi();
    }

    @Override
    public void setDiaChi(String diaChi) {
        super.setDiaChi(diaChi);
    }
}
